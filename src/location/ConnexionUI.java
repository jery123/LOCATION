    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
           
            String queryB="SELECT * FROM `bailleur` WHERE emailB=? AND pwdB=?";
            String queryL="SELECT * FROM `locataire` WHERE emailL=? AND pwdL=?";
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/location", "root", "");
           
            patL =con.prepareStatement(queryL);
            patB =con.prepareStatement(queryB);
            patL.setString(1, email.getText());
            patL.setString(2, pwd.getText());
            patB.setString(1, email.getText());
            patB.setString(2, pwd.getText());
            //            pat.setString(3, String.valueOf(role.getSelectedItem()));
            rs=patB.executeQuery();

            if(rs.next()){
                JOptionPane.showMessageDialog(this,"Vous etes  connecté ! ");
                // return true;
                tableaubayeur dashb=new tableaubayeur();
                dashb.emailB.setText(rs.getString("emailB"));
                dashb.n_p_bailleur.setText(rs.getString("nomB")+" "+rs.getString("prenomB"));
                dashb.setVisible(true);
                this.setVisible(false);
            }
            else{
                    rs=patL.executeQuery();
                   if(rs.next()){
                      JOptionPane.showMessageDialog(this,"Vous etes connecté ! ");
                // return true;
                locataire dashb=new locataire();
                dashb.nomL.setText(rs.getString("nomL"));
                dashb.emailL.setText(rs.getString("emailL"));
                dashb.setVisible(true);
                this.setVisible(false);
            }else{
                     JOptionPane.showMessageDialog(this,"Aucun compte ne correspond à l'adresse "+email.getText());   
                   }
                // return false;
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

